//! Per-build binfmt_misc registration for emulated systems.
//!
//! Kernel 6.7+ gives each user namespace that mounts binfmt_misc its
//! own empty instance, so the sandbox registers a qemu-user
//! interpreter for itself without touching host state. The static
//! interpreter is bound at [`INTERP_PATH`] and registered with the F
//! flag (opened at registration, survives pivot_root).

/// Where the static emulator binary is bound inside the sandbox.
pub const INTERP_PATH: &str = "/run/binfmt-emulator";

/// `register` file line for `system`, or None if unknown.
pub fn register_line(system: &str) -> Option<String> {
    let (magic, mask) = magic(system)?;
    Some(format!(":tribuchet:M:0:{magic}:{mask}:{INTERP_PATH}:F"))
}

